#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	if (n == 0)
		return (0);
	while (n--)
	{
		if (*s1 != *s2++)
			return (*(const unsigned char*)s1 - *(const unsigned char *)--s2);
		if (*s1++ == EOS)
			break ;
	}
	return (0);
}
